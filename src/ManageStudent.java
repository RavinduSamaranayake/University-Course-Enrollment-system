    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Connection con = MyConnection.getconnection();
        PreparedStatement ps = null;
        if(txt_studentid.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Student Id can't be Empty.");
        }
        else if(txt_email.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Student email can't be Empty.");
        }
         else if(txt_firstname.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Student name can't be Empty.");
        }
           
          else if(chooser_dob.getDate()==null){
            JOptionPane.showMessageDialog(null, "Student date of birth can't be Empty.");
        }
          else if(chooser_enroll.getDate()==null){
            JOptionPane.showMessageDialog(null, "Student enroll date can't be Empty.");
         }
           
       
        //if(String.valueOf(jPasswordField1.getPassword()) == String.valueOf(jPasswordField2.getPassword())){
            //lblPas.setVisible(false);
      //  }
         
        else if(chooser_dob.getDate().compareTo(new Date())>0){
            JOptionPane.showMessageDialog(null, "No Student from the future are allowed.");
        }
        else{
        String sid = txt_studentid.getText();
        String fname = txt_firstname.getText();
        String lname = txt_surname.getText();
        String email = txt_email.getText();
        String zscore = txt_zscore.getText();
        String qulify = txt_qulification.getText();
        String facname = txt_fac.getText();
        String semid = txt_semid.getText();
         
        SimpleDateFormat dateformat = new SimpleDateFormat("yyyy-MM-dd");
        String bdate = dateformat.format(chooser_dob.getDate());
        String enroldate = dateformat.format(chooser_enroll.getDate());
        
        
        String query = "UPDATE `student` SET `fname`=?,`lname`=? ,`email`=?,`dob`=?,`zscore`=?,`qtype`=?,`facname`=?,`enroldate`=?,`semid`=? WHERE `stid`=?";
        try {
            ps = con.prepareStatement(query);
            
            ps.setString(1, fname);
            ps.setString(2, lname);
             
            ps.setString(3, email);
            ps.setString(4, bdate);
            ps.setString(5, zscore);
            ps.setString(6, qulify);
            ps.setString(7, facname);
            ps.setString(8, enroldate);
            ps.setString(9, semid);
            ps.setString(10, sid);
         if(ps.executeUpdate()>0){
         JOptionPane.showMessageDialog(null, "Student is Updated");
         
         jTable1.setModel(new DefaultTableModel(null,new Object[]{"Student ID", "First name", "Surname", "Date of Birth", "Email", "Faculty", "Enroll Date", "Semester ID", "Z score", "Qulification Type"}));
         stt.fillstjtable(jTable1,"");
         stt.fillstjtable(jTable1,txt_search.getText());
         
         }else{
         JOptionPane.showMessageDialog(null, "Not Complete or Insert wrong details");
         }
            
        } catch (SQLException ex) {
            //Logger.getLogger(SignupStudent.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        }
                                            

     
            
    }//GEN-LAST:event_jButton3ActionPerformed

