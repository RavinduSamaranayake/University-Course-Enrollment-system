    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int rowIndex = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        txt_studentid.setText(model.getValueAt(rowIndex, 0).toString());
        txt_firstname.setText(model.getValueAt(rowIndex, 1).toString());
        txt_surname.setText(model.getValueAt(rowIndex, 2).toString());
        txt_email.setText(model.getValueAt(rowIndex, 4).toString());
        txt_fac.setText(model.getValueAt(rowIndex, 5).toString());
        txt_semid.setText(model.getValueAt(rowIndex, 7).toString());
        txt_zscore.setText(model.getValueAt(rowIndex, 8).toString());
        txt_qulification.setText(model.getValueAt(rowIndex, 9).toString());
        Date bdate,endate;
        try {
            bdate = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rowIndex, 3).toString());
            endate = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rowIndex, 6).toString());
            chooser_dob.setDate(bdate);
            chooser_enroll.setDate(endate);
        } catch (ParseException ex) {
            Logger.getLogger(ManageStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

